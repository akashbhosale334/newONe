package casting;

public class Cast_test {

	public static void main(String[] args) 
	{
int a=452;
{
	System.out.println("value of a is "+a);
	}
double b=a;
{
	System.out.println("value of b is "+b);
	}
double c=452.25;
{
	System.out.println("value of c is "+c);
	}
int d=(int) c;
{
	System.out.println("value of d is "+d);}
	}

}
